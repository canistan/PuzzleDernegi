                            <h3>TÜRKİYE PUZZLE HIZ YARIŞMASI - TARİHÇE </h3>
                            
                            <br>
                            
							<p>14 Aralık 2014 tarihinde 1. Dünya Puzzle Hız Yarışması düzenlendi. Yarışmaya Almanya, Rusya, Türkiye, Danimarka, İngiltere, Arjantin, Singapur’dan dünyanın en hızlı puzzle (yap-boz) severleri katıldı. Dünyanın her yerinde aynı anda başlayan yarışmada, tüm yarışmacılar yarışma için önceden belirlenmiş olan 1000 parçalık puzzle’ı en hızlı şekilde yapmaya başladı ve yarışmanın galibi 3 saat 8 dakika 5 saniye ile Almanya’dan Luna oldu. İkinciliği Rusya 3 saat 41 dakika aldı. 3.lük 4 saat 41 dakika ile İlknur Sürmeli- Türkiye’den ve 4.’lük 5 saat 5 dakikalık derecesi ile Pelin Çelik-Türkiye’den geldi. Sırasıyla Danimarka, İngiltere, Arjantin, Singapur ve Türkiye’den Rana Altınok yarışmayı tamamladılar.
							</p>
							<p>Facebook ve gerek diğer sosyal paylaşım ortamlarında geniş ilgi toplayan bu yarışmanın farklı bir versiyonu 22 Mart 2015 tarihinde Türkiye’de düzenlendi. Türkiye İş Güvenliği İş Adamları Derneği (TİGİAD) ve Özensan A.Ş. tarafından düzenlenen 1. Türkiye Puzzle Hız Yarışması’nda, <strong>Sabır ve Dikkat Sanatı olan Yap-Boz (Puzzle)</strong>’ları en hızlı yapan kişi 5 saat 5 dakika ile Yalova’dan İlknur Sürmeli oldu ve Türkiye Puzzle Hız Şampiyonu ünvanını aldı. Yarışma, TİGİAD’ın 3. Yönetim Kurulu Başkanı ve Ombudsman’ı rahmetli Engin Çelik’e ithaf edildi.
							</p>
							<p>2015 yılında İş Sağlığı ve Güvenliği’ne dikkat çekmeyi hedefleyen TİGİAD ile Avusturya markası Piatnik ve İngiliz markası Wentworth ahşap puzzle’ların Türkiye Mümessili olan Özensan A.Ş.-Hobi Grubu tarafından organize edilen  yarışmaya Türkiye’nin 19 ilinden toplam 35 yarışmacı katıldı. TİGİAD’ın, Çalışma ve Sosyal Güvenlik Bakanlığı tarafından 25.7.2012’de yayınlanan Kişisel Koruyucu Donanımlarla ilgili Uyumlaştırılmış Ulusal Standartlara ilişkin özel bir çalışmasının yarışmanın ana unsuru olduğu yarışmada, tüm yarışmacılar aynı anda 1000 parçalık yap-boz (puzzle)’ı yapmaya başladılar. Türkiye’nin dört bir yanından, 18 yaşından 70 yaşına kadar en hızlı puzzle’cıların (yap boz) katıldığı yarışma, İstanbul’da Olivium Outlet Center’de düzenlendi. Avusturya’da hazırlanan özel bir program ile canlı olarak internet üzerinden takip edildi, yarışmacılara arkadaşları, destekçileri, bilgisayar ortamında kutlama ve mesaj iletti, her yarışmacının o an itibariyle kaç parça koyduğu, kimin önde olduğu anında takip edildi. Kısaca halk arasında yarışmaya interaktif katılım sağlandı.Yarışmada derece alanlara organizasyon tarafından plaket ve çeşitli ödüller verildi.
							</p>
							<p>22 Mart 2015 tarihindeki 1.Türkiye Puzzle Hız Yarışması, 7’den 70’e her yaşta insanın severek yaptığı Yap-Boz (Puzzle) ile ilgili Türkiye çapında ilk ve tek yarışma olmasının yanı sıra, 2014 yılında yaşanan Soma faciasından yola çıkarak TİGİAD’ın desteği ve özel çalışması ile halkın ürün güvenliği ve Çalışma ve Sosyal Güvenlik Bakanlığı’nın koymuş olduğu standartlarla ilgili bilinçlendirilmesini amaçlamaktaydı. Hedef, ‘Sabır ve Dikkat Sanatı Yap-boz’ oyunu ile, keyifli ve zevkli bir şekilde tüm Türkiye’de İş Sağlığı ve Güvenliği konusunda bilgi paylaşımını sağlamaktı.
							</p>
							<p>27 Mart 2016 tarihinde düzenlenen 2. Türkiye Puzzle Hız Yarışması’nda yarışma ön eleme ve Final olarak iki etapta yapıldı. Türkiye’de özellikle Istanbul’da yaşanan terör saldırıları nedeniyle, 90 yarışmacıdan sadece 39’u Torium Alışveriş merkezindeki ön elemeye katıldı. Seçmeler sonucunda finale kalan 20 puzzle’cı 27 Mart tarihinde KatsSahne’de düzenlenen finallerde yarıştı ve 2. Türkiye Puzzle Hız Yarışması Şampiyonu Dilara Düğmeci , 2. İzmir’den Semiha Güngör ve 3. Nalan Muratbay oldu. Engin Çelik Özel Ödülü her türlü rahatsızlığa rağmen engel tanımayan Bandırma’dan Emine Taphasan’a verildi.
							</p>
							<p>3.Türkiye Puzzle Hız Yarışması, 10 Aralık 2016 tarihinde İstanbul Beşiktaş’daki hain terör saldırısında henüz 28 yaşındayken hayatını yitiren Selin Çelik’e ithaf edildi. Saldırıda katledilen 38 kişi arasındaki Selin Çelik o kısacık hayatında kelebekleri çok sevmiş ve kaldırıldığı hastanede vucudundaki kelebek dövmesi sayesinde kimliği tespit edilebilmişti. Bu sebeple, yarışma için kelebek görseli kullanıldı ve ‘Kelebek Selin’ adlı özel üretim puzzle,  yarısma sonrası puzzle severlerle bulusturuldu. Kelebek Selin puzzle’ının gelirinin bir kısmı Avusturya Liseliler Derneği’nin bünyesinde Görkem Yazıcı&Selin Çelik Eğitim Bursu’na aktarılmaktadır.   3. Türkiye Puzzle Hız Yarışması’nda 1. Nalan Muratbay 2. Dilara Düğmeci ve 3. Meryem Toprak oldu. Engin Çelik Özel Ödülü Nezih Rodoplu’ya ve Selin Çelik Özel Ödülü de Emine Taphasan’a verildi.
							</p>
							<p>Ana teması ‘Sabır ve Dikkat Sanatı Yap-Boz’u daha geniş kitlelere duyurmayı ve Türkiye’nin tüm illerinden en hızlı puzzlecıları biraraya getirmeyi amaçlayan Türkiye Puzzle Hız Yarışması’nın 4.sü 25 Mart 2018 tarihinde düzenlenecek. Yarışma Engin Çelik ve Selin Çelik Özel Ödülleri’ni almış olan ve 2017 senesinde vefat eden, puzzle camiasının sevilen ismi Sn. Emine Taphasan’a ithaf edildi. Yarışmanın bu seneki Onur Konukları Dünya Puzzle Günleri’ni düzenleyen Avusturyalı Günther Simetsberger ve Meksikalı Emma Gonzalez de Martinez. Bu sene ayrıca Dünyanın ve Türkiye’nin önde gelen puzzle’cılarının katılacağı ‘Puzzle All Star’ gerçekleştirilecek.
							</p>


