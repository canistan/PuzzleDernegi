								<p>Derneğin Defter Tutma Esas ve Usulleri ve Tutulacak Defterler</p>
								
								<p>Madde 17- Defter tutma esasları;</p>
								
								<p>Dernekte, işletme hesabı esasına göre defter tutulur. Ancak, 
									yıllık brüt gelirin Dernekler Yönetmeliğinin 31. Maddesinde 
									belirtilen haddi aşması durumunda takip eden hesap 
									döneminden başlayarak bilanço esasına göre defter tutulur. 
									Bilanço esasına geçilmesi durumunda, üst üste iki hesap 
									döneminde yukarıda belirtilen haddin altına düşülürse, takip
									eden yıldan itibaren işletme hesabı esasına dönülebilir. 
									Yukarıda belirtilen hadde bağlı kalmaksızın yönetim kurulu
									kararı ile bilanço esasına göre defter tutulabilir. Derneğin 
									ticari işletmesi açılması durumunda, bu ticari işletme için, 
									ayrıca Vergi Usul Kanunu hükümlerine göre defter tutulur.</p>
									
								<p>Kayıt Usulü</p>
									
								<p>Derneğin defter ve kayıtları Dernekler Yönetmeliğinde belirtilen 
									usul ve esasa uygun olarak tutulur.</p>
									
								<p>Tutulacak Defterler</p>
								
								<p>Dernekte, aşağıda yazılı defterler tutulur.</p>
								
								<p>i. İşletme hesabı esasında tutulacak defterler ve uyulacak 
									esaslar aşağıdaki gibidir:</p>
									
								<p>1. Karar Defteri: Yönetim kurulu kararları tarih ve numara 
									sırasıyla bu deftere yazılır ve kararların altı toplantıya 
									katılan üyelerce imzalanır.</p>
								<p>2. Üye Kayıt Defteri: Derneğe üye olarak girenlerin kimlik 
									bilgileri, derneğe giriş ve çıkış tarihleri bu deftere işlenir. 
									Üyelerin ödedikleri giriş ve yıllık aidat miktarları bu deftere 
									işlenebilir.</p>
									
								<p>3. Evrak Kayıt Defteri: Gelen ve giden evraklar, tarih ve sıra 
									numarası ile bu deftere kaydedilir. Gelen evrakın asılları ve 
									giden evrakın kopyaları dosyalanır. Elektronik posta yoluyla 
									gelen veya giden evraklar çıktısı alınmak suretiyle saklanır.
									</p>
									
								<p>4. Demirbaş Defteri: Derneğe ait demirbaşların edinme tarihi ve 
									şekli ile kullanıldıkları veya verildikleri yerler ve kullanım 
									sürelerini dolduranların kayıttan düşürülmesi bu deftere 
									işlenir.</p>
									
								<p>5. İşletme Hesabı Defteri: Dernek adına alınan gelirler ve 
									yapılan giderler açık ve düzenli olarak bu deftere işlenir</p>
									
								<p>6. Alındı Belgesi Kayıt Defteri: Alındı belgelerinin seri ve sıra 
									numaraları, bu belgeleri alan ve iade edelerin adı, soyadı ve 
									imzaları ile aldıkları ve iade ettikleri tarihler bu deftere 
									işlenir.</p>
									
								<p>ii. Bilanço esasında tutulacak defterler ve uyulacak esaslar 
									aşağıdaki gibidir:</p>
									
								<p>1. (a) bendinin 1, 2, 3 ve 6’cı alt bentlerinde kayıtlı defterler 
									bilanço esasında defter tutulması durumunda da tutulur.</p>
									
								<p>2. Yevmiye Defteri, Büyük Defter ve Envanter Defteri: Bu 
									defterlerin tutulma usulü ile kayıt şekli Vergi Usul Kanunu ile 
									bu Kanununun Maliye Bakanlığına verdiği yetkiye istinaden 
									yayımlanan Muhasebe Sistemi Uygulama Genel Tebliğleri esaslarına 
									göre yapılır.</p>
									
								<p>Defterlerin Tasdiki</p>
								
								<p>Dernekte, tutulması zorunlu olan defterler kullanmaya başlamadan 
									önce il dernekler müdürlüğüne veya notere tasdik ettirilir. Bu 
									defterlerin kullanılmasına sayfaları bitene kadar devam edilir 
									ve defterlerin ara tasdiki yapılmaz. Ancak, bilanço esasına göre 
									tutulan defterler ile form veya sürekli form yapraklı 
									defterlerin, kullanılacağı yıldan önce gelen son ayda, her 
									yıl yeniden tasdik ettirilmesi zorunludur.</p>
									
								<p>Gelir Tablosu ve Bilanço Düzenlenmesi</p>
								
								<p>İşletme hesabı esasına göre kayıt tutulması durumunda yıl 
									sonlarında (31 Aralık) (Dernekler Yönetmeliği EK-16’da 
									belirtilen) “İşletme Hesabı Tablosu” düzenlenir. Bilanço esasına 
									göre defter tutulması durumunda ise, yıl sonlarında (31 Aralık), 
									Maliye Bakanlığınca yayımlanan Muhasebe Sistemi Uygulama Genel 
									Tebliğlerini esas alarak bilanço ve gelir tablosu düzenlenir.
									</p>
								 
								<p>Derneğin Gelir ve Gider İşlemleri</p>
								
								<p>Madde 18- Gelir ve gider belgeleri;</p>
								
								<p>Dernek gelirleri, (Dernekler Yönetmeliği EK- 17’de örneği 
									bulunan) “Alındı Belgesi” ile tahsil edilir. Dernek gelirlerinin 
									bankalar aracılığı ile tahsili halinde banka tarafından 
									düzenlenen dekont veya hesap özeti gibi belgeler alındı belgesi 
									yerine geçer. Dernek giderleri ise fatura, perakende satış fişi, 
									serbest meslek makbuzu gibi harcama belgeleri ile yapılır. 
									Ancak derneğin, Gelir Vergisi Kanununun 94’üncü maddesi 
									kapsamında bulunan ödemeleri için Vergi Usul Kanunu hükümlerine 
									göre gider pusulası, bu kapsamda da bulunmayan ödemeleri için 
									(Dernekler Yönetmeliği EK-13’te örneği bulunan) “Gider Makbuzu” 
									düzenlenir. Dernek tarafından kişi, kurum veya kuruluşlara 
									yapılacak bedelsiz mal ve hizmet teslimleri (Dernekler 
									Yönetmeliği EK-14’te örneği bulunan) “Ayni Yardım Teslim 
									Belgesi” ile yapılır. Kişi, kurum veya kuruluşlar tarafından 
									derneğe yapılacak bedelsiz mal ve hizmet teslimleri ise 
									(Dernekler Yönetmeliği EK-15’te örneği bulunan) “Ayni Bağış 
									Alındı Belgesi” ile kabul edilir.</p>
									
								<p>Alındı Belgeleri</p>
									
								<p>Dernek gelirlerinin tahsilinde kullanılacak “Alındı 
									Belgeleri” (Dernekler Yönetmeliği EK- 17’de gösterilen biçim ve 
									ebatta) yönetim kurulu kararıyla, matbaaya bastırılır. Alındı 
									belgelerinin bastırılması ve kontrolü, matbaadan teslim 
									alınması, deftere kaydedilmesi, eski ve yeni saymanlar arasında 
									devir teslimi ve alındı belgesi ile dernek adına gelir tahsil 
									edecek kişi veya kişiler tarafından bu alındı belgelerinin 
									kullanımına ve toplanılan gelirlerin teslimine ilişkin 
									hususlarda Dernekler Yönetmeliğinin ilgili hükümlerine göre 
									hareket edilir.</p>
									
								<p>Yetki Belgesi</p>
								
								<p>Dernek adına gelir tahsil edecek kişi veya kişiler, yetki süresi 
									de belirtilmek suretiyle, yönetim kurulu kararı ile tespit 
									edilir. Gelir tahsil edecek kişilerin açık kimliği, imzası ve 
									fotoğraflarını ihtiva eden (Dernekler Yönetmeliği EK- 19’da 
									örneği bulunan) “Yetki Belgesi“ dernek tarafından üç nüsha 
									olarak düzenlenerek, dernek yönetim kurulu başkanınca onaylanır. 
									Yetki belgelerinin birer sureti dernekler birimlerine verilir. 
									Yetkibelgesi ile ilgili değişi klikler yönetim kurulu 
									başkanınca, on beş gün içerisinde dernekler birimine bildirilir. 
									Dernek adına gelir tahsil edecek kişiler, ancak adlarına 
									düzenlenen yetki belgelerinin bir suretinin dernekler 
									birimine verilmesinden itibaren gelir tahsil etmeye 
									başlayabilirler. Yetki belgesinin kullanımı, yenilenmesi, iadesi 
									ve sair hususlarda Dernekler Yönetmeliğinin ilgili hükümlerine 
									göre hareket edilir.</p>
									
								<p>Belgelerinin Saklama Süresi</p>
								
								<p>Defterler hariç olmak üzere, dernek tarafından kullanılan alındı 
									belgeleri, harcama belgeleri ve diğer belgeler özel kanunlarda 
									belirtilen süreler saklı kalmak üzere, kaydedildikleri 
									defterlerdeki sayı ve tarih düzenine uygun olarak 5 yıl süreyle 
									saklanır.</p>
									
								<p>Beyanname Verilmesi</p>
								
								<p>Madde 19- Derneğin, bir önceki yıla ait faaliyetleri ile gelir ve 
									gider işlemlerinin yıl sonu itibarıyla sonuçlarına ilişkin 
									(Dernekler Yönetmeliği EK-21’de sunulan) “Dernek Beyannamesi” 
									dernek yönetim kurulu tarafından doldurarak, her takvim yılının 
									ilk dört ayı içinde dernek başkanı tarafından mahallin mülki 
									idare amirliğine verilir.</p>
								 
								<p>Bildirim Yükümlülüğü</p>        
								<p>Madde 20- Mülki amirliğe yapılacak bildirimler;</p>
								<p>Genel Kurul Sonuç Bildirimi</p>
								
								<p>Olağan veya olağanüstü genel kurul toplantılarını izleyen otuz 
									gün içinde, yönetim ve denetim kurulları ile diğer organlara 
									seçilen asıl ve yedek üyeleri içeren (Dernekler Yönetmeliği EK- 
									3’ te sunulan)  “Genel Kurul Sonuç Bildirimi” ve ekleri yönetim 
									kurulu başkanı tarafından mülki idare amirliğine bildirilir:</p>
									
								<p>Genel kurul sonuç bildirimine;</p>
								
								<p>1- Divan başkanı, başkan yardımcıları ve yazman tarafından 
									imzalanmış genel kurul toplantı tutanağı örneği,</p>
									
								<p>2- Tüzük değişikliği yapılmışsa, tüzüğün değişen maddelerinin
									yeni ve eski şekli ile dernek tüzüğünün son şeklinin her sayfası 
									yönetim kurulunca imzalanmış örneği Eklenir.</p>
								 
								<p>Taşınmazların Bildirilmesi</p>
								
								<p>Derneğin edindiği taşınmazlar tapuya tescilinden itibaren otuz 
									gün içinde (Dernekler Yönetmeliği EK-26’da sunulan) “Taşınmaz 
									Mal Bildirimi”ni doldurmak suretiyle mülki idare amirliğine 
									bildirilir.</p>
									
								<p>Yurtdışından Yardım Alma Bildirimi</p>
								
								<p>Dernek tarafından, yurtdışından yardım alınacak olması durumunda 
									yardım alınmadan önce (Dernekler Yönetmeliği EK-4’te belirtilen) 
									“Yurtdışından Yardım Alma Bildirimi” iki nüsha olarak doldurup 
									mülki idare amirliğine bildirimde bulunurlar. Bildirim formuna, 
									yurt dışından yardım alınması hususunda alınmış yönetim kurulu 
									kararı örneği, varsa bu konuda düzenlenen protokol, sözleşme ve 
									benzeri belgeler ile yardımın aktarıldığı hesaba ilişkin dekont, 
									ekstra ve benzeri belgenin bir örneği de eklenir. Nakdi 
									yardımların bankalar aracılığıyla alınması ve 
									kullanılmadan önce bildirim şartının yerine getirilmesi 
									zorunludur.</p>
									
								<p>Kamu Kurum ve Kuruluşları İle Birlikte Yürütülen Ortak Projelerle 
									ilgili Bildirim Derneğin görev alanına ilişkin konularda kamu 
									kurum ve kuruluşları ile yürüttüğü ortak projelerle ilgili 
									olarak yapılan protokol ve projenin örneği (Dernekler 
									Yönetmeliği EK- 23’de gösterilen) “Proje Bildirimi”ne eklenerek, 
									protokol tarihini izleyen bir ay içinde dernek merkezinin 
									bulunduğu yerin valiliğine verilir.</p>
									
								<p>Değişikliklerin Bildirilmesi</p>
								
								<p>Derneğin yerleşim yerinde meydana gelen değişiklik (Dernekler 
									Yönetmeliği EK-24’te belirtilen) “Yerleşim Yeri Değişiklik 
									Bildirimi”; genel kurul toplantısı dışında dernek 
									organlarında meydana gelen değişiklikler (Dernekler Yönetmeliği 
									EK-25’te belirtilen) “Dernek Organlarındaki Değişiklik 
									Bildirimi” doldurulmak suretiyle, değişikliği izleyen otuz 
									gün içinde mülki idare amirliğine bildirilir. Dernek tüzüğünde 
									yapılan değişiklikler de tüzük değişikliğinin yapıldığı genel 
									kurul toplantısını izleyen otuz gün içinde, genel kurul sonuç 
									bildirimi ekinde mülki idare amirliğine bildirilir.</p>
									
								<p>Derneğin İç Denetimi</p>
								
								<p>Madde 21- Dernekte genel kurul, yönetim kurulu veya denetim 
									kurulu tarafından iç denetim yapılabileceği gibi, bağımsız 
									denetim kuruluşlarına da denetim yaptırılabilir. Genel kurul, 
									yönetim kurulu veya bağımsız denetim kuruluşlarınca denetim 
									yapılmış olması, denetim kurulunun yükümlülüğünü ortadan 
									kaldırmaz. Denetim kurulu tarafından en geç yılda bir defa 
									derneğin denetimi gerçekleştirilir. Genel kurul veya yönetim 
									kurulu, gerek görülen hallerde denetim yapabilir veya bağımsız 
									denetim kuruluşlarına denetim yaptırabilir.</p>
									
								<p>Derneğin Borçlanma Usulleri</p>
								
								<p>Madde 22- Dernek amacını gerçekleştirmek ve faaliyetlerini 
									yürütebilmek için ihtiyaç duyulması halinde yönetim kurulu 
									kararı ile borçlanma yapabilir. Bu borçlanma kredili mal ve 
									hizmet alımı konularında olabileceği gibi nakit olarak da 
									yapılabilir. Ancak bu borçlanma, derneğin gelir kaynakları ile 
									karşılanamayacak miktarlarda ve derneği ödeme güçlüğüne 
									düşürecek nitelikte yapılamaz. En son kesin hesabın yarısını 
									geçmeyecek şekilde yönetim kurulu bunun üstündeki borçlanmalarda 
									Genel Kurul yetkilidir. Proje karşılığı Genel Kurulun 
									yönetim kuruluna verebileceği borçlanma yetkisi hakkı saklıdır.
									</p>
								 
								<p>Tüzüğün Ne Şekilde Değiştirileceği</p>
								<p>Madde 23- Tüzük değişikliği genel kurul kararı ile yapılabilir.
									</p>
									
								<p>Genel kurulda tüzük değişikliği yapılabilmesi için genel kurula 
									katılma hakkı bulunan üyelerin 2/3 çoğunluğu aranır. Çoğunluğun 
									sağlanamaması sebebiyle toplantının ertelenmesi durumunda ikinci 
									toplantıda çoğunluk aranmaz. Ancak, bu toplantıya katılan üye 
									sayısı, yönetim ve denetim kurulları üye tam sayısının iki 
									katından az olamaz</p>
									
								<p>Tüzük değişikliği için gerekli olan karar çoğunluğu toplantıya 
									katılan ve oy kullanma hakkı bulunan üyelerin oylarının 
									2/3’ü’dür. Genel kurulda tüzük değişikliği oylaması açık olarak 
									yapılır.</p>
									
								<p>Derneğin  Feshi ve  Mal Varlığının  Tasfiye   Şekli</p>
								
								<p>Madde 24- Genel kurul, her zaman derneğin feshine karar 
									verebilir.</p>
								<p>Genel kurulda fesih konusunun görüşülebilmesi için genel kurula 
									katılma hakkı bulunan üyelerin 2/3 çoğunluğu aranır. Çoğunluğun 
									sağlanamaması sebebiyle toplantının ertelenmesi durumunda ikinci 
									toplantıda çoğunluk aranmaz. Ancak, bu toplantıya katılan üye 
									sayısı, yönetim ve denetim kurulları üye tam sayısının iki 
									katından az olamaz.</p>
								
								<p>Fesih kararının alınabilmesi için gerekli olan karar çoğunluğu 
									toplantıya katılan ve oy kullanma hakkı bulunan üyelerin 
									oylarının 2/3’ü’dür. Genel kurulda fesih kararı oylaması açık 
									olarak yapılır.</p>
								
								<p>Tasfiye İşlemleri</p>
								
								<p>Genel kurulca fesih kararı verildiğinde, derneğin para, mal ve 
									haklarının tasfiyesi son yönetim kurulu üyelerinden oluşan 
									tasfiye kurulunca yapılır. Bu işlemlere, feshe ilişkin genel 
									kurul kararının alındığı veya kendiliğinden sona erme halinin 
									kesinleştiği tarihten itibaren başlanır.</p>
									
								<p>Tasfiye süresi içinde bütün işlemlerde dernek adında “Tasfiye 
									Halinde Yapboz Derneği” ibaresi kullanılır. Tasfiye kurulu, 
									mevzuata uygun olarak derneğin para, mal ve haklarının tasfiyesi 
									işlemlerini baştan sonuna kadar tamamlamakla görevli ve 
									yetkilidir. Bu kurul, önce derneğin hesaplarını inceler. 
									İnceleme esnasında derneğe ait defterler, alındı belgeleri, 
									harcama belgeleri, tapu ve banka kayıtları ile diğer 
									belgelerinin tespiti yapılarak varlık ve yükümlülükleri bir 
									tutanağa bağlanır. Tasfiye işlemeleri sırasında derneğin 
									alacaklılarına çağrıda bulunulur ve varsa malları paraya 
									çevrilerek alacaklılara ödenir. Derneğin alacaklı olması 
									durumunda alacaklar tahsil edilir. Alacakların tahsil edilmesi 
									ve borçların ödenmesinden sonra kalan tüm para, mal ve hakları, 
									genel kurulda belirlenen yere devredilir. Genel kurulda, 
									devredilecek yer belirlenmemişse derneğin bulunduğu ildeki 
									amacına en yakın ve fesih edildiği tarihte en fazla üyeye sahip 
									derneğe devredilir.</p>
								
								<p>Tasfiyeye ilişkin tüm işlemler tasfiye tutanağında gösterilir ve 
									tasfiye işlemleri, mülki idare amirliklerince haklı bir nedene 
									dayanılarak verilen ek süreler hariç üç ay içinde tamamlanır. 
									Derneğin para, mal ve haklarının tasfiye ve intikal işlemlerinin 
									tamamlanmasını müteakip tasfiye kurulu tarafından durumun yedi 
									gün içinde bir yazı ile dernek merkezinin bulunduğu yerin mülki 
									idare amirliğine bildirilmesi ve bu yazıya tasfiye tutanağının 
									da eklenmesi zorunludur. Derneğin defter ve belgelerini tasfiye 
									kurulu sıfatıyla son yönetim kurulu üyeleri saklamakla 
									görevlidir. Bu görev, bir yönetim kurulu üyesine de verilebilir. 
									Bu defter ve belgelerin saklanma süresi beş yıldır.</p>
									
								<p>Hüküm Eksikliği</p>
								
								<p>Madde 25- Bu tüzükte belirtilmemiş hususlarda Dernekler Kanunu, 
									Türk Medeni Kanunu ve bu Kanunlara atfen çıkartılmış olan 
									Dernekler Yönetmeliği ve ilgili diğer mevzuatın dernekler 
									hakkındaki hükümleri uygulanır.</p>
									
								<p>Bu tüzük 25 (Yirmibeş) maddeden ibarettir.</p>



