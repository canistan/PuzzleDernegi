							<p>	Yüksek İstişare Konseyi, Görev ve Yetkileri</p>
							<p> Madde 15–  Yüksek İstişare Konseyi, Derneğin en üst danışma
								organıdır.</p>
							<p>Yüksek İstişare Konseyi, Genel Kurul tarafından seçilen Asil, Fahri
								ve Onursal üyelerden olmak üzere toplam 5 üyeden oluşur. Bir önceki
								dönem yönetim kurulu başkanı ve geçmiş dönem başkanları kurulun 
								doğal üyesidir. Geçmiş dönem başkan sayısının 5’den fazla olması 
								durumunda üyeler genel kurul tarafından geçmiş dönem başkanları 
								arasından seçilir.</p>
								
							<p>	Yüksek İstişare Konseyi’nin diğer üyeleri derneğin en az beş (5) 
								sene üyesi olan üyeleri arasından dernek genel kurulu tarafından 
								seçilir.  Yüksek İstişare Konseyinin üç üyeden oluşan Başkanlık 
								Divanı, Genel Kurul toplantısında iki yıllık bir süre için seçilir
								Başkanlık Divanı, bir başkan, bir başkan vekili ve bir sekreter 
								üyeden oluşur.  Konsey Başkanının daveti ile uygun gördüğü 
								tarihlerde toplanarak konularını müzakere eder ve karara bağlar.
								Ayrıca yönetim kurulunun talep etmesi halinde Yönetim Kurulu 
								Başkanının daveti ile olağanüstü de toplanabilir.</p>
								
								<p>Yüksek İstişare Konseyi başkanı aynı zamanda derneğin yayın
									organı olan derginin olağan yayın kurulu üyesidir. Yüksek 
									İstişare Konseyi başkan dışında en az bir üyesini daha derginin 
									yayın kurulu üyesi olarak seçer. Yüksek İstişare Konseyi, 
									Yönetim Kurulunun onay vermesi halinde daha fazla üyeyi de 
									derginin yayın kurulu üyeliğine atayabilir.</p>
									
								<p>Yapboz Derneği’nin (PuzzleDer) Ombudsmanı iki yıllık süre için 
									Yüksek İstişare Kurulu tarafından Yüksek İstişare Kurulu üyeleri 
									arasından seçilir.</p>
									
								<p>Konsey kararları istişari nitelikte olup, Yönetim Kurulunu 
									bağlamaz. Yüksek İstişare Konseyi toplantılarına gündemin 
									özelliğine göre, Yasama, Yürütme, Yargı organlarının 
									temsilcileri, sivil toplum örgütleri, kamu tüzel kişiliğini haiz 
									meslek örgütleri ve bunların üst örgütleri, akademik kurumların 
									mensupları ile Konsey üyesi olmayan dernek üyeleri görüş 
									alışverişinde bulunulmak üzere Konsey Başkanlığınca davet 
									edilebilirler.</p>
									
								<p>Konsey yazışmaları dernek sekreterliğince yerine getirilir. 
									Konsey Başkanı ve üyeleri isterlerse, yönetim kurulu 
									toplantılarına oy hakkı olmaksızın katılabilirler.</p>
									
								<p>Konseyin başlıca görev ve yetkileri</p>
								
								<p>i. Yapboz (puzzle) hayatının genel gidişi ve sorunlarını gözden 
									geçirmek ve uzun vadeli projelere ışık tutmak,</p>
								<p>ii. Dernek amaçlarına en uygun şekilde ulaşabilmek maksadı ile 
									hazırlanacak stratejileri değerlendirmek ve tavsiyelerde 
									bulunmak,</p>
								<p>iii. Dernek çalışmalarını amaçlara uygunluk ve netice almadaki 
									etkenlikler bakımından incelemek ve gerekli önlemleri tavsiye 
									etmek</p>
								<p>iv. Derneğin dergisine birisi Yüksek İstişare Kurulu Başkanı 
									olmak üzere iki üye atamak</p>
								<p> v. Yapboz Derneği’nin Ombudsmanını atamak</p>
								
