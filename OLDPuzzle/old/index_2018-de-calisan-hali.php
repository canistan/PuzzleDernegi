									<p>*Yarışmanın Başlangıç saati 10:30’dur. Organizasyonun başlangıç talimatıyla yarış başlar.</p>
									<p>*  Final yarışmasının süresi 7 saattir.</p>
									<p><strong>*</strong> Her oyuncu bireysel olarak yarışacaktır. Yarışmacıların dışardan, 2. Şahıslardan puzzle parçalarının ayrıştırılması, dizilmesi, birleştirilmesi vs. gibi bir konuda destek alması kesinlikle yasaktır. Bu tür destek alan yarışmacı diskalifiye edilecektir.</p>
