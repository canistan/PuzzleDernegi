									<p>*Seçmelerde  500 parçalık puzzle’ı tamamlayan ilk 20 kişi 25 Mart 2018’de İstanbul’da yapılacak olan Final’e kalmaya hak kazanacaktır. </p>
									
									<br><br>
									<h3>2. AŞAMA: FİNAL - 25 MART 2018 - DENİZ MÜZESİ </h3>
									<p>*Türkiye Hız Yarışması Final’ine Puzzle All Star’daki Türk puzzle’cılar ile seçmelerde ilk 20’ye giren puzzle’cılar katılacaktır.</p>
									<p>*Her yarışmacı için masa hazırlanacaktır. </p>
									<p>*Yarışma Puzzle’ı  1000 parçalık olup, tüm Finalistler aynı puzzle ile yarışacaktır.</p>
									<p>* Yarışmacılara,  yarışma puzzle’ı Yarışın Başlangıcından önce masalarına kutuların üst tarafı görünmeyecek şekilde ters olarak dağıtılacaktır. </p>
